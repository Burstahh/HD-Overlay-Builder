    public static string T(string key, string lang)
    {
        string code = IsSupportedLanguage(lang) ? lang : "en";
        if (ExtraText.TryGetValue(code, out var map) && map.TryGetValue(key, out var translated)) return translated;
        if (!Text.TryGetValue(key, out var item)) return key;
        return IsSpanish(code) ? item.es : item.en;
    }

    public static string PresetLabel(string id, string es, string en, string lang)
    {
        string key = "preset_" + id;
        string translated = T(key, lang);
        if (!string.Equals(translated, key, StringComparison.OrdinalIgnoreCase)) return translated;
        return IsSpanish(lang) ? es : en;
    }

    private static string ByLang(string lang, string en, string es, string pt, string fr, string de, string it)
    {
        string code = IsSupportedLanguage(lang) ? lang.ToLowerInvariant() : "en";
        return code switch
        {
            "es" => es,
            "pt" => pt,
            "fr" => fr,
            "de" => de,
            "it" => it,
            _ => en
        };
    }

